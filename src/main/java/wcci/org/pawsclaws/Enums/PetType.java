package wcci.org.pawsclaws.Enums;

/**
 * Enum representing the types of pets available in the system.
 * This enum contains both regular pets and robotic pets.
 */
public enum PetType {
    /** Represents a real dog. */
    Dog,

    /** Represents a real cat. */
    Cat,

    /** Represents a robotic dog. */
    RoboDog,

    /** Represents a robotic cat. */
    RoboCat
}

